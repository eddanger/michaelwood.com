export default function motdCommand() {
  const artFunctions = [bloody, shadow, threeDee];
  const randomArt = artFunctions[Math.floor(Math.random() * artFunctions.length)]();

  // Define your ASCII art MOTD as a multi-line template literal.
  // https://patorjk.com/software/taag/#p=display&v=0&f=Bloody&t=Michael%20Wood
  let motd = randomArt + String.raw`

Welcome to my terminal!
Type 'help' to see available commands.
  `;

  return motd;
}

